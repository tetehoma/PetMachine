public class Pet {
    private String name;
    private Boolean clean;

    public Pet(String name, Boolean clean) {
        this.name = name;
        this.clean = clean;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Boolean getClean() {
        return clean;
    }

    public void setClean(Boolean clean) {
        this.clean = clean;
    }

   


}
